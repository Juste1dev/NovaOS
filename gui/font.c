int font_char_width(font_size_t size) {
    switch(size) {
        case FONT_SMALL:  return FONT_SM_W;
        case FONT_LARGE:  return FONT_LG_W;
        case FONT_TITLE:  return FONT_TITLE_W;
        default:          return FONT_W;
    }
}

int font_char_height(font_size_t size) {
    switch(size) {
        case FONT_SMALL:  return FONT_SM_H;
        case FONT_LARGE:  return FONT_LG_H;
        case FONT_TITLE:  return FONT_TITLE_H;
        default:          return FONT_H;
    }
}

static int font_append_text(char *dst, int di, int max, const char *rep) {
    int i = 0;
    while (rep && rep[i] && di < max - 1) dst[di++] = rep[i++];
    dst[di] = 0;
    return di;
}

static int font_map_utf8_char(const unsigned char *s, char *out, int outmax) {
    if (!s || !s[0] || outmax < 2) return 0;
    out[0] = '?'; out[1] = 0;

    if (s[0] < 0x80) {
        out[0] = (char)s[0];
        out[1] = 0;
        return 1;
    }

    if (s[0] == 0xC3) {
        switch (s[1]) {
            case 0x80: case 0x81: case 0x82: case 0x83: case 0x84: case 0x85: out[0] = 'A'; return 2;
            case 0x86: out[0] = 'A'; out[1] = 'E'; out[2] = 0; return 2;
            case 0x87: out[0] = 'C'; return 2;
            case 0x88: case 0x89: case 0x8A: case 0x8B: out[0] = 'E'; return 2;
            case 0x8C: case 0x8D: case 0x8E: case 0x8F: out[0] = 'I'; return 2;
            case 0x91: out[0] = 'N'; return 2;
            case 0x92: case 0x93: case 0x94: case 0x95: case 0x96: case 0x98: out[0] = 'O'; return 2;
            case 0x99: case 0x9A: case 0x9B: case 0x9C: out[0] = 'U'; return 2;
            case 0x9D: out[0] = 'Y'; return 2;
            case 0xA0: case 0xA1: case 0xA2: case 0xA3: case 0xA4: case 0xA5: out[0] = 'a'; return 2;
            case 0xA6: out[0] = 'a'; out[1] = 'e'; out[2] = 0; return 2;
            case 0xA7: out[0] = 'c'; return 2;
            case 0xA8: case 0xA9: case 0xAA: case 0xAB: out[0] = 'e'; return 2;
            case 0xAC: case 0xAD: case 0xAE: case 0xAF: out[0] = 'i'; return 2;
            case 0xB1: out[0] = 'n'; return 2;
            case 0xB2: case 0xB3: case 0xB4: case 0xB5: case 0xB6: case 0xB8: out[0] = 'o'; return 2;
            case 0xB9: case 0xBA: case 0xBB: case 0xBC: out[0] = 'u'; return 2;
            case 0xBD: case 0xBF: out[0] = 'y'; return 2;
            default: break;
        }
    }

    if (s[0] == 0xC5) {
        if (s[1] == 0x92) { out[0] = 'O'; out[1] = 'E'; out[2] = 0; return 2; }
        if (s[1] == 0x93) { out[0] = 'o'; out[1] = 'e'; out[2] = 0; return 2; }
    }

    if (s[0] == 0xE2) {
        if (s[1] == 0x80) {
            switch (s[2]) {
                case 0x93: case 0x94: out[0] = '-'; return 3;
                case 0x98: case 0x99: out[0] = '\''; return 3;
                case 0x9C: case 0x9D: out[0] = '"'; return 3;
                case 0xA2: out[0] = '*'; return 3;
                case 0xA6: out[0] = '.'; out[1] = '.'; out[2] = '.'; out[3] = 0; return 3;
                default: break;
            }
        }
        if (s[1] == 0x86) {
            switch (s[2]) {
                case 0x90: out[0] = '<'; return 3;
                case 0x91: out[0] = '^'; return 3;
                case 0x92: out[0] = '>'; return 3;
                case 0x93: out[0] = 'v'; return 3;
                case 0xBB: out[0] = 'R'; return 3;
                default: break;
            }
        }
        if (s[1] == 0x8C && s[2] == 0x82) { out[0] = 'H'; return 3; }
        if (s[1] == 0x9C && (s[2] == 0x93 || s[2] == 0x94)) { out[0] = 'v'; return 3; }
    }

    return ((s[0] & 0xF0) == 0xE0) ? 3 : 2;
}

static void font_sanitize_utf8(const char *src, char *dst, int max) {
    int si = 0, di = 0;
    char mapped[8];
    if (!dst || max <= 0) return;
    dst[0] = 0;
    while (src && src[si] && di < max - 1) {
        int adv = font_map_utf8_char((const unsigned char *)&src[si], mapped, sizeof(mapped));
        if (adv <= 0) adv = 1;
        di = font_append_text(dst, di, max, mapped);
        si += adv;
    }
    dst[di] = 0;
}

int font_string_width(const char *str, font_size_t size) {
    char clean[2048];
    int w = 0;
    font_sanitize_utf8(str ? str : "", clean, sizeof(clean));
    for (int i = 0; clean[i]; i++) w += font_char_width(size);
    return w;
}

static int font_sample_coverage(const uint8_t *glyph, int px, int py, int cw, int ch_h) {

    static const int off2[4][2] = {
        {64,64},{192,64},{64,192},{192,192}
    };
    static const int off3[9][2] = {
        {42,42},{128,42},{214,42},
        {42,128},{128,128},{214,128},
        {42,214},{128,214},{214,214}
    };
    static const int off4[16][2] = {
        {32,32},{96,32},{160,32},{224,32},
        {32,96},{96,96},{160,96},{224,96},
        {32,160},{96,160},{160,160},{224,160},
        {32,224},{96,224},{160,224},{224,224}
    };

    const int (*off)[2] = off2;
    int total = 4;
    if (cw >= FONT_TITLE_W - 1 || ch_h >= FONT_TITLE_H - 2) {
        off = off4;
        total = 16;
    } else if (cw >= FONT_LG_W || ch_h >= FONT_LG_H || cw > FONT_W || ch_h > FONT_H) {
        off = off3;
        total = 9;
    }

    int hits = 0;
    for (int i = 0; i < total; i++) {
        int sx_fp = ((px * 256) + off[i][0]) * 8  / (cw   ? cw   : 1);
        int sy_fp = ((py * 256) + off[i][1]) * 16 / (ch_h ? ch_h : 1);
        int sx = sx_fp >> 8;
        int sy = sy_fp >> 8;
        if (sx < 0) sx = 0; else if (sx > 7)  sx = 7;
        if (sy < 0) sy = 0; else if (sy > 15) sy = 15;
        hits += (glyph[sy] >> (7 - sx)) & 1;
    }
    if (hits <= 0) return 0;
    if (hits >= total) return 255;
    return (hits * 255 + total / 2) / total;
}

void font_draw_char(int x, int y, char ch, color_t fg, color_t bg, font_size_t size) {
    if (ch < 0) return;
    unsigned char c = (unsigned char)ch;
    const uint8_t *glyph = font8x16[c];
    int cw = font_char_width(size);
    int ch_h = font_char_height(size);
    int embolden = (size == FONT_BOLD);

    for (int py = 0; py < ch_h; py++) {
        for (int px = 0; px < cw; px++) {
            int cov = font_sample_coverage(glyph, px, py, cw, ch_h);
            if (embolden && px > 0) {
                int prev = font_sample_coverage(glyph, px - 1, py, cw, ch_h);
                if (prev > cov) cov = prev;
            }
            if (cov <= 0 && bg == COLOR_TRANS) continue;

            int rx = x + px;
            int ry = y + py;
            if ((unsigned)rx >= vbe.width || (unsigned)ry >= vbe.height) continue;

            color_t base = (bg == COLOR_TRANS) ? vbe_get_pixel(rx, ry) : bg;
            if (cov <= 0) {
                vbe_put_pixel(rx, ry, base);
                continue;
            }

            int alpha = cov;
            if (size == FONT_SMALL) alpha += 6;
            if (size == FONT_LARGE) alpha += 18;
            if (size == FONT_TITLE) alpha += 24;
            if (cov > 0 && cov < 104) alpha = (alpha * 7) / 8;
            if (alpha < 0) alpha = 0;
            if (alpha > 255) alpha = 255;
            vbe_put_pixel(rx, ry, blend_color(fg, base, (uint8_t)alpha));
        }
    }
}

void font_draw_string(int x, int y, const char *str, color_t fg, color_t bg, font_size_t size) {
    char clean[2048];
    int cx = x;
    font_sanitize_utf8(str ? str : "", clean, sizeof(clean));
    for (int i = 0; clean[i]; i++) {
        if (clean[i] == '\n') { y += font_char_height(size); cx = x; continue; }
        if (clean[i] == '\t') { cx += font_char_width(size) * 4; continue; }
        font_draw_char(cx, y, clean[i], fg, bg, size);
        cx += font_char_width(size);
    }
}

void font_draw_string_shadow(int x, int y, const char *str, color_t fg, font_size_t size) {
    font_draw_string(x+1, y+1, str, RGB(0,0,0), COLOR_TRANS, size);
    font_draw_string(x, y, str, fg, COLOR_TRANS, size);
}
